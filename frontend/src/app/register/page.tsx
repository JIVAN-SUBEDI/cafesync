function HotelDetailsStep({
  formData,
  updateFormData,
  errors,
  setFieldError,
  clearFieldError,
}: {
  formData: any;
  updateFormData: (field: any, value: any) => void;
  errors: Record<string, string>;
  setFieldError: (field: string, error: string) => void;
  clearFieldError: (field: string) => void;
}) {
  const [slugPreview, setSlugPreview] = useState("");
  const [imageError, setImageError] = useState("");
  const hasAutoGeneratedRef = useRef(false);
  const prevHotelNameRef = useRef(formData.hotel_name);

  useEffect(() => {
    if (!formData.hotel_name.trim()) {
      setFieldError("hotel_name", "Hotel name is required");
    } else {
      clearFieldError("hotel_name");
    }
  }, [formData.hotel_name, setFieldError, clearFieldError]);

  useEffect(() => {
    if (!formData.hotel_slug.trim()) {
      setFieldError("hotel_slug", "URL slug is required");
    } else {
      clearFieldError("hotel_slug");
    }
  }, [formData.hotel_slug, setFieldError, clearFieldError]);

  useEffect(() => {
    if (prevHotelNameRef.current !== formData.hotel_name) {
      prevHotelNameRef.current = formData.hotel_name;

      const slug = formData.hotel_name
        .toLowerCase()
        .replace(/[^\w\s-]/g, "")
        .replace(/\s+/g, "-")
        .replace(/--+/g, "-")
        .trim();

      setSlugPreview(slug);

      if (
        !formData.hotel_slug &&
        formData.hotel_name &&
        !hasAutoGeneratedRef.current
      ) {
        hasAutoGeneratedRef.current = true;
        updateFormData("hotel_slug", slug);
      }
    }
  }, [formData.hotel_name, formData.hotel_slug, updateFormData]);

  const imagePreview = useMemo(() => {
    if (!formData.image) return null;
    return URL.createObjectURL(formData.image);
  }, [formData.image]);

  useEffect(() => {
    return () => {
      if (imagePreview) URL.revokeObjectURL(imagePreview);
    };
  }, [imagePreview]);

  const handleImageChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0] || null;

    if (!file) {
      updateFormData("image", null);
      setImageError("");
      clearFieldError("image");
      return;
    }

    if (!file.type.startsWith("image/")) {
      updateFormData("image", null);
      setImageError("Please select a valid image file.");
      setFieldError("image", "Please select a valid image file.");
      e.target.value = "";
      return;
    }

    const maxSizeInBytes = 2 * 1024 * 1024;
    if (file.size > maxSizeInBytes) {
      updateFormData("image", null);
      setImageError("Image size must be 2MB or less.");
      setFieldError("image", "Image size must be 2MB or less.");
      e.target.value = "";
      return;
    }

    updateFormData("image", file);
    setImageError("");
    clearFieldError("image");
  };

  return (
    <div className="space-y-6">
      <div>
        <h3 className="text-2xl font-bold text-gray-900 mb-2">
          Cafe/Resturant Information
        </h3>
        <p className="text-gray-600">Tell us about your Cafe or restaurant</p>
      </div>

      <div className="space-y-4">
        <div>
          <label className="block text-sm font-medium text-gray-700 mb-2">
            Cafe/Restaurant Name *
          </label>
          <input
            type="text"
            value={formData.hotel_name}
            onChange={(e) => updateFormData("hotel_name", e.target.value)}
            className={`w-full px-4 py-3 rounded-xl border ${
              errors.hotel_name ? "border-red-300" : "border-gray-300"
            } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors`}
            placeholder="Grand Hotel & Resort"
          />
          {errors.hotel_name && (
            <p className="mt-1 text-sm text-red-600">{errors.hotel_name}</p>
          )}
        </div>

        <div>
          <label className="block text-sm font-medium text-gray-700 mb-2">
             URL Slug *
          </label>
          <div className="flex items-center gap-3">
            <span className="text-gray-500 whitespace-nowrap">
              CloudCafe.com/
            </span>
            <input
              type="text"
              value={formData.hotel_slug}
              onChange={(e) => {
                updateFormData("hotel_slug", e.target.value);
                hasAutoGeneratedRef.current = false;
              }}
              className={`flex-1 px-4 py-3 rounded-xl border ${
                errors.hotel_slug ? "border-red-300" : "border-gray-300"
              } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors`}
              placeholder="grand-hotel-resort"
            />
          </div>
          {errors.hotel_slug && (
            <p className="mt-1 text-sm text-red-600">{errors.hotel_slug}</p>
          )}
          <p className="mt-1 text-sm text-gray-500">
            This will be your unique URL. Example: CloudCafe.com/
            {slugPreview || "your-hotel"}
          </p>
        </div>

        <div>
          <label className="block text-sm font-medium text-gray-700 mb-2">
            Hotel Image
          </label>

          <input
            type="file"
            accept="image/*"
            onChange={handleImageChange}
            className={`w-full px-4 py-3 rounded-xl border ${
              imageError || errors.image ? "border-red-300" : "border-gray-300"
            } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors file:mr-4 file:rounded-lg file:border-0 file:bg-amber-50 file:px-4 file:py-2 file:text-sm file:font-medium file:text-amber-700 hover:file:bg-amber-100`}
          />

          {imageError && (
            <p className="mt-1 text-sm text-red-600">{imageError}</p>
          )}

          {!imageError && errors.image && (
            <p className="mt-1 text-sm text-red-600">{errors.image}</p>
          )}

          <p className="mt-1 text-sm text-gray-500">
            Upload JPG, PNG, or WebP image up to 2MB.
          </p>

          {formData.image && imagePreview && (
            <div className="mt-4">
              <p className="text-sm font-medium text-gray-700 mb-2">Preview</p>
              <div className="w-40 h-40 rounded-xl border border-gray-200 overflow-hidden bg-gray-50">
                <img
                  src={imagePreview}
                  alt="Hotel preview"
                  className="w-full h-full object-cover"
                />
              </div>
              <div className="mt-2 flex items-center justify-between text-sm text-gray-600">
                <span className="truncate max-w-[220px]">
                  {formData.image.name}
                </span>
                <button
                  type="button"
                  onClick={() => {
                    updateFormData("image", null);
                    setImageError("");
                    clearFieldError("image");
                  }}
                  className="text-red-600 hover:text-red-700 font-medium"
                >
                  Remove
                </button>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

