function AdminDetailsStep({
  formData,
  updateFormData,
  errors,
  setFieldError,
  clearFieldError,
}: {
  formData: any;
  updateFormData: (field: any, value: any) => void;
  errors: Record<string, string>;
  setFieldError: (field: string, error: string) => void;
  clearFieldError: (field: string) => void;
}) {
  const [showPassword, setShowPassword] = useState(false);
  const [showConfirmPassword, setShowConfirmPassword] = useState(false);
  const [passwordStrength, setPasswordStrength] = useState("");

  useEffect(() => {
    if (!formData.admin_name.trim()) setFieldError("admin_name", "Admin name is required");
    else clearFieldError("admin_name");
  }, [formData.admin_name, setFieldError, clearFieldError]);

  useEffect(() => {
    if (!formData.admin_email.trim()) setFieldError("admin_email", "Email is required");
    else clearFieldError("admin_email");
  }, [formData.admin_email, setFieldError, clearFieldError]);

  useEffect(() => {
    if (!formData.admin_phone.trim()) setFieldError("admin_phone", "Phone number is required");
    else clearFieldError("admin_phone");
  }, [formData.admin_phone, setFieldError, clearFieldError]);

  useEffect(() => {
    if (!formData.admin_password) {
      setFieldError("admin_password", "Password is required");
      setPasswordStrength("");
      return;
    }

    clearFieldError("admin_password");

    const hasUpper = /[A-Z]/.test(formData.admin_password);
    const hasLower = /[a-z]/.test(formData.admin_password);
    const hasNumber = /\d/.test(formData.admin_password);
    const hasSpecial = /[^\w\s]/.test(formData.admin_password);
    const length = formData.admin_password.length;

    if (length >= 8 && hasUpper && hasLower && hasNumber && hasSpecial) {
      setPasswordStrength("Strong");
    } else if (
      length >= 8 &&
      ((hasUpper && hasLower) || (hasLower && hasNumber) || (hasUpper && hasNumber))
    ) {
      setPasswordStrength("Medium");
    } else if (length > 0) {
      setPasswordStrength("Weak");
    } else {
      setPasswordStrength("");
    }
  }, [formData.admin_password, setFieldError, clearFieldError]);

  useEffect(() => {
    if (!formData.admin_confirm_password) {
      if (formData.admin_password) {
        setFieldError("admin_confirm_password", "Please confirm your password");
      } else {
        clearFieldError("admin_confirm_password");
      }
      return;
    }

    if (formData.admin_password !== formData.admin_confirm_password) {
      setFieldError("admin_confirm_password", "Passwords do not match");
    } else {
      clearFieldError("admin_confirm_password");
    }
  }, [
    formData.admin_password,
    formData.admin_confirm_password,
    setFieldError,
    clearFieldError,
  ]);

  const getPasswordStrengthColor = () => {
    switch (passwordStrength) {
      case "Strong":
        return "text-green-600";
      case "Medium":
        return "text-yellow-600";
      case "Weak":
        return "text-orange-600";
      default:
        return "text-gray-500";
    }
  };

  return (
    <div className="space-y-6">
      <div>
        <h3 className="text-2xl font-bold text-gray-900 mb-2">Admin Account</h3>
        <p className="text-gray-600">Create your administrator account</p>
      </div>

      <div className="space-y-4">
        <div>
          <label className="block text-sm font-medium text-gray-700 mb-2">
            Full Name *
          </label>
          <input
            type="text"
            value={formData.admin_name}
            onChange={(e) => updateFormData("admin_name", e.target.value)}
            className={`w-full px-4 py-3 rounded-xl border ${
              errors.admin_name ? "border-red-300" : "border-gray-300"
            } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors`}
            placeholder="John Smith"
          />
          {errors.admin_name && (
            <p className="mt-1 text-sm text-red-600">{errors.admin_name}</p>
          )}
        </div>

        <div>
          <label className="block text-sm font-medium text-gray-700 mb-2">
            Email Address *
          </label>
          <input
            type="email"
            value={formData.admin_email}
            onChange={(e) => updateFormData("admin_email", e.target.value)}
            className={`w-full px-4 py-3 rounded-xl border ${
              errors.admin_email ? "border-red-300" : "border-gray-300"
            } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors`}
            placeholder="admin@yourhotel.com"
          />
          {errors.admin_email && (
            <p className="mt-1 text-sm text-red-600">{errors.admin_email}</p>
          )}
        </div>

        <div>
          <label className="block text-sm font-medium text-gray-700 mb-2">
            Phone Number *
          </label>
          <input
            type="tel"
            value={formData.admin_phone}
            onChange={(e) => updateFormData("admin_phone", e.target.value)}
            className={`w-full px-4 py-3 rounded-xl border ${
              errors.admin_phone ? "border-red-300" : "border-gray-300"
            } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors`}
            placeholder="1234567890"
          />
          {errors.admin_phone && (
            <p className="mt-1 text-sm text-red-600">{errors.admin_phone}</p>
          )}
          <p className="mt-1 text-sm text-gray-500">
            Enter 10-digit phone number (numbers only)
          </p>
        </div>

        <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-2">
              Password *
            </label>
            <div className="relative">
              <input
                type={showPassword ? "text" : "password"}
                value={formData.admin_password}
                onChange={(e) =>
                  updateFormData("admin_password", e.target.value)
                }
                className={`w-full px-4 py-3 rounded-xl border ${
                  errors.admin_password ? "border-red-300" : "border-gray-300"
                } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors pr-12`}
                placeholder="••••••••"
              />
              <button
                type="button"
                onClick={() => setShowPassword((prev) => !prev)}
                className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-500 hover:text-gray-700"
              >
                {showPassword ? <EyeOff size={20} /> : <Eye size={20} />}
              </button>
            </div>
            {errors.admin_password && (
              <p className="mt-1 text-sm text-red-600">
                {errors.admin_password}
              </p>
            )}
            {formData.admin_password &&
              !errors.admin_password &&
              passwordStrength && (
                <p className={`mt-1 text-sm ${getPasswordStrengthColor()}`}>
                  Password strength: {passwordStrength}
                </p>
              )}
            <p className="mt-1 text-xs text-gray-500">
              Must contain: 8+ chars, uppercase, lowercase, number, special
              character
            </p>
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-2">
              Confirm Password *
            </label>
            <div className="relative">
              <input
                type={showConfirmPassword ? "text" : "password"}
                value={formData.admin_confirm_password}
                onChange={(e) =>
                  updateFormData("admin_confirm_password", e.target.value)
                }
                className={`w-full px-4 py-3 rounded-xl border ${
                  errors.admin_confirm_password
                    ? "border-red-300"
                    : "border-gray-300"
                } focus:ring-2 focus:ring-amber-500 focus:border-transparent transition-colors pr-12`}
                placeholder="••••••••"
              />
              <button
                type="button"
                onClick={() => setShowConfirmPassword((prev) => !prev)}
                className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-500 hover:text-gray-700"
              >
                {showConfirmPassword ? <EyeOff size={20} /> : <Eye size={20} />}
              </button>
            </div>
            {errors.admin_confirm_password && (
              <p className="mt-1 text-sm text-red-600">
                {errors.admin_confirm_password}
              </p>
            )}
          </div>
        </div>
      </div>
    </div>
  );
}

